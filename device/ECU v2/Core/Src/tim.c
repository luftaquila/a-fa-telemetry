extern uint32_t timer_flag;
extern uint32_t ic_flag;
extern int32_t ic_value[IC_CH_COUNT];
extern int32_t ic_buffer_0[IC_CH_COUNT];
extern int32_t ic_buffer_1[IC_CH_COUNT];

int32_t ic_armed = false;
static void CALCULATE_PERIOD(int channel, uint32_t arr);

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
  static uint32_t count = 0;

  if (htim->Instance == TIM1) {
    count++;

    timer_flag |= 1 << TIMER_100ms;

    if (!(count & 0x3)) {
      timer_flag |= 1 << TIMER_400ms;
    }

    if (count == 10) {
      timer_flag |= 1 << TIMER_1s;
      count = 0;
    }
  }
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
  if (htim->Instance != TIM5) {
    return;
  }

  if (ic_armed == true) {
    // HAL_TIM_IC_Start_DMA(&htim3, TIM_CHANNEL_1, (uint32_t *)&ic_buffer_1[IC_WHEEL_FL], 1);
    // HAL_TIM_IC_Start_DMA(&htim3, TIM_CHANNEL_2, (uint32_t *)&ic_buffer_1[IC_WHEEL_FR], 1);
    HAL_TIM_IC_Start_DMA(&htim3, TIM_CHANNEL_3, (uint32_t *)&ic_buffer_1[IC_WHEEL_RL], 1);
    HAL_TIM_IC_Start_DMA(&htim3, TIM_CHANNEL_4, (uint32_t *)&ic_buffer_1[IC_WHEEL_RR], 1);

    ic_armed = false;
  } else {
    uint32_t arr = htim->Instance->ARR;
    uint32_t ch = 0xBADACAFE;

    switch (htim->Channel) {
      case HAL_TIM_ACTIVE_CHANNEL_1:
        ch = IC_WHEEL_FL;
        break;

      case HAL_TIM_ACTIVE_CHANNEL_2:
        ch = IC_WHEEL_RL;
        break;

      case HAL_TIM_ACTIVE_CHANNEL_3:
        ch = IC_WHEEL_FR;
        break;

      case HAL_TIM_ACTIVE_CHANNEL_4:
        ch = IC_WHEEL_RR;
        break;

      default:
        break;
    }

    if (ch != 0xBADACAFE) {
      ic_flag |= 1 << ch;
      CALCULATE_PERIOD(ch, arr);
    }
  }
}

